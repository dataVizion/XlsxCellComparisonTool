    private void jCompareButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCompareButtonActionPerformed
        this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        messageLabel.setText("");
        if(fA==null || fB==null){
            messageLabel.setText("Please select .xlsx files");
        }
        else{
            //System.out.println("Starting");
            try {                
                ExcelCell[][] resA=OfficeUtils.getInstance().compareFiles(fA,jSheetFileA.getSelectedIndex(), fB,jSheetFileB.getSelectedIndex());
                ResultFrame res=new ResultFrame(resA);
                //this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));                
                res.setVisible(true);
                res.setTitle(fA.getName()+"["+jSheetFileA.getSelectedItem()+"]"+" VS "+fB.getName()+"["+jSheetFileB.getSelectedItem()+"]");
                res.setjLabelFileAText(fA.getName()+"["+jSheetFileA.getSelectedItem()+"]");
                res.setjLabelFileBText(fB.getName()+"["+jSheetFileB.getSelectedItem()+"]");
                
                System.gc();
            } catch (Exception ex) {
                Logger.getLogger(ComparisonFrame.class.getName()).log(Level.SEVERE, null, ex);
                //this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
                this.pack();
                messageLabel.setText("Please select .xlsx files");
            }
        }
        this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR)); 
    }//GEN-LAST:event_jCompareButtonActionPerformed

