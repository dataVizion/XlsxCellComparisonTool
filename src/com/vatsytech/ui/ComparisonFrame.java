    private void initDropList (File fA,java.awt.Choice j) throws Exception{
        String[] lSheet=OfficeUtils.getInstance().getSheetNamesFromXlsx(fA);
        j.removeAll();
        for(int i=0;i<lSheet.length;i++){
            j.add(lSheet[i]);
        }      
        j.setEnabled(true);
    }
    


