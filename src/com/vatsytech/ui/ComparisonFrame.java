    private void jButtonFileAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFileAActionPerformed
        messageLabel.setText("");
        try{            
        FileChooser a=new FileChooser(this,true,fA);
        a.setVisible(true);
        fA=a.getFile();        
        textFileA.setText(fA.getName());       
        initDropList(fA,jSheetFileA);
        }        catch(Exception e){
            e.printStackTrace();
            System.out.println("File exception while choosing file A");
            jSheetFileA.removeAll();
            jSheetFileA.setEnabled(false);
            messageLabel.setText("Please select .xlsx files");
        }
    }//GEN-LAST:event_jButtonFileAActionPerformed

